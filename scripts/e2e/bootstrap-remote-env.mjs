#!/usr/bin/env node
/**
 * Write .env.e2e.local for remote Supabase E2E (gitignored).
 * Uses: supabase projects api-keys --project-ref <ref> -o json
 *
 * Usage: node scripts/e2e/bootstrap-remote-env.mjs
 */
import { execSync } from "node:child_process"
import { writeFileSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const root = resolve(dirname(fileURLToPath(import.meta.url)), "../..")
const outPath = resolve(root, ".env.e2e.local")
const projectRef = process.env.SUPABASE_PROJECT_REF ?? "oouswalwqhojpzqwwdvs"
const baseUrl = `https://${projectRef}.supabase.co`

const raw = execSync(
  `supabase projects api-keys --project-ref ${projectRef} -o json`,
  { encoding: "utf8" }
)
const keys = JSON.parse(raw)
const anon = keys.find((k) => k.name === "anon")?.api_key
const service = keys.find((k) => k.name === "service_role")?.api_key

if (!anon || !service) {
  console.error("Could not read anon/service_role from supabase CLI")
  process.exit(1)
}

const lines = [
  "# Auto-generated by scripts/e2e/bootstrap-remote-env.mjs — do not commit",
  `NEXT_PUBLIC_SUPABASE_URL=${baseUrl}`,
  `NEXT_PUBLIC_SUPABASE_ANON_KEY=${anon}`,
  `SUPABASE_SERVICE_ROLE_KEY=${service}`,
  "",
]

writeFileSync(outPath, lines.join("\n"))
console.log(`Wrote ${outPath}`)
console.log(`Remote: ${baseUrl}`)
